public class SplachActivity extends Activity {

	private static final String SHARED_PREF_NAME = "MemoryApp"; 
	private static final String SHARED_PREF_USER = "username";
	private EditText username_et;
	
	  /** Duration of wait **/
    private final int SPLASH_DISPLAY_LENGTH = 2000;

    /** Called when the activity is first created. */
    @Override
    public void onCreate(Bundle icicle) {
        super.onCreate(icicle);
        setContentView(R.layout.activity_splash);

        /* New Handler to start the Menu-Activity 
         * and close this Splash-Screen after some seconds.*/
        new Handler().postDelayed(new Runnable(){
            @Override
            public void run() {
                /* Create an Intent that will start the Menu-Activity. */
                Intent mainIntent = new Intent(SplachActivity.this,MainmenuActivity.class);
                SplachActivity.this.startActivity(mainIntent);
                SplachActivity.this.finish();
            }
        }, SPLASH_DISPLAY_LENGTH);
    }
	
	 
}




